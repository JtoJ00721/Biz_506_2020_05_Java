package com.biz.hello;

public class VarString_01 {
	
	public static void main(String[] args) {
		
		int num1 = 10;
		
		
		char char1 = 'A';
		
		String str1 = "Are you Experienced?";
		/*
		 * 일반적인 프로그래밍 언어에서는
		 * 문자(영문, 한글 1글자) 를 저장하는 방식은 모두 가능하다.
		 * 
		 * 하지만 문자열(2글자 이상의 문자들이 묶여있는)은
		 * 프로그래밍 언어마다 다소 다른 방식으로 취급을 한다.
		 * 
		 * 전통적인 c 언어 환경에서는 문자열을 취급하는데
		 * 상당회 어려운 방법을 사용했었다.
		 * 
		 * 자바 초기에는 문자열을 취급하는 방법이 없었는데
		 * String이라는 특별한 클래스형 키워드를 만들어서
		 * 매우 쉽게 문자열을 취급할수 있도록 하였다.
		 * 
		 * 자바는 생산성(개발을 얼마나 빠리, 쉽게, 오류가 없이)에
		 * 중점을 두고 탄생한 언어다 보니
		 * 원래 설게당시에 없던 기능들이 매우 쉽게 추가할 수 있다.
		 * 이러한 철학에 바탕을 두고 탄생한 기능이
		 * String형 값 취급 방법이다.
		 */
		System.out.println(str1);
		
	}

}
